# Dictionary is an unordered collection of key-value pairs.
# It is generally used when we have a huge amount of data.
# Dictionaries are optimized for retrieving data.
d = {1: 'value',  'key': 2}
print(type(d))

# Use key to retrieve the respective value
print("d[1] = ", d[1])

print("d['key'] = ", d['key'])