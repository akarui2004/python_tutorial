# List is an ordered sequence of items.
# It is one of the most used datatype in Python and is very flexible
a = [1, 2.2, 'python']

# Slicing Operator [] to extract an item or a range of items
a = [5,10,15,20,25,30,35,40,45]

# a[2] = 15
print("a[2] = ", a[2])

# a[0:3] = [5,10,15]
print("a[0:3] = ", a[0:3])

# a[5:] = [30, 35, 40, 45]
print("a[5:] = ", a[5:])